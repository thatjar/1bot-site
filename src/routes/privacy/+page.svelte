<h1>1Bot Privacy Policy</h1>
<h2>Privacy is an inalienable human right, and we respect it.</h2>
<p>
  As for 1Bot itself, it does not have any Privileged Gateway Intents (which
  include the Message Content intent) and is technically incapable of reading
  any messages in any Discord server unless it is @mentioned. It is capable of
  reading direct messages to it, but it does not log or store any.
</p>

<h2>The only data we collect</h2>
<p>
  When you use any of 1Bot's commands, it does not log or store anything unless
  the command results in an unexpected error. The only data that will be logged
  in case of an unhandled error is the command itself and all the parameters you
  provide to it (if any) as well as your Discord User ID. This is only to
  improve the bot, and ensure that the user who invokes the command in question
  is not a malicious actor. We only consider the user a malicious actor if they
  deliberately spam the command such that it results in an error, in an attempt
  to rate limit the bot and get it blocked by Discord's API.
</p>
<p>
  For some commands that connect to third parties, the data you include in the
  command will be sent to the third party. A list of third parties that 1Bot
  connects to can be found <a href="/third-parties">here</a>.
</p>

<p>
  You can see the source code of <a href="https://github.com/thatjar/1bot"
    >1Bot</a
  >
  or <a href="https://github.com/thatjar/1bot-site">this website</a> for yourself.
  IPs are not collected, zero cookies are used.
</p>

